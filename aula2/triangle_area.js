// Pede as informações de base e altura do triângulo
var base = parseFloat(prompt("Digite a base do triângulo (em metros):  "));
var altura = parseFloat(prompt("Digite a altura do triângulo (em metros): "));
var area = (base * altura) / 2;

// Imprime o resultado da área
console.log("A área do triângulo é: " + area + " metros quadrados");
